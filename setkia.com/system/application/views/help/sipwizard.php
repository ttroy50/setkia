
<div id="help">
    <p>
    <h3>Wizard Settings</h3>
    <hr />
    </p>
    <p>
    <b>
    Name
    </b>
    <br />
    Indicates a logical, user readable, identity of the SIP settings.
    </p>
    <p>
    <b>
    App Reference
    </b>
    <br />
    This is the logical name that your settings will be known as. This is used to link your settings to other settings
    e.g. VoIP Settings.
    <br />
    The App Reference should be unique.
    </p>

    <p>
    <b>
    Username
    </b>
    <br />
    Username for this SIP Account
    </p>

    <p>
    <b>
    Password
    </b>
    <br />
    Password for this SIP Account
    </p>

    <p>
    <b>
    Registration
    </b>
    <br />
    Indicates if automatic registration is on or off.
    </p>

    <p>
    <b>
    Proxy Server Address
    </b>
    <br />
    Address of the SIP Server / outbound proxy. Valid values are IPV4/6 address, domain or FQDN. Value SHOULD be given without sip:
    or sips: prefix.
    </p>


    <p>
    <b>
    Domain
    </b>
    <br />
    Domain of the SIP Server / outbound proxy. Valid values are IPV4/6 address, domain or FQDN. Value SHOULD be given without sip:
    or sips: prefix.
    </p>

    <p>
    <b>
    Realm
    </b>
    <br />
    Realm of the outbound proxy / registrar. Note that this parameter must be
    exactly the same text string that the proxy server returns in the realm parameter of the
    Proxy-Authenticate header of 407 response to REGISTER.
    </p>

    <p>
    <b>
    Port
    </b>
    <br />
    Port number on SIP Server
    </p>

    <p>
    <b>
    Transport Type
    </b>
    <br />
    Transport protocol to be used between terminal and SIP Server
    If this parameter is set to auto, the transport protocol will be automatically selected.
    </p>






</div>
