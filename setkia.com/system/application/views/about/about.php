        <h3>About SetKia</h3>
        <p>
            <b>What is Setkia?</b>
            <br />
            SetKia is an application to provision settings to Nokia phones using OTA provisioning techneques.
            We can help you to both create and provision settings for Access Points, Browsers, SIP Settings etc.
            For Users we can provide preconfigured settings for popular settings and for service providers we provide an easy to use
            method to setup new users for your service.
        </p>
        <p>
            <b>What is OTA Provisioning?</b>
            <br />
            OTA (Over-The-Air) provisioning is a technique to send settings to your phone via SMS. We create and encoded message
            using standard WAP Provisioning techniques and send this to a phone. The user can then click on this message to accept
            the new \ updated settings.
        </p>
